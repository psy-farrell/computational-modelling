v <- list(c(1,1,1,1,1,1,1,1),
          c(1,1,1,1,-1,-1,-1,-1),
          c(1,1,-1,-1,1,1,-1,-1),
          c(1,1,-1,-1,-1,-1,1,1),
          c(1,-1,1,-1,1,-1,1,-1),
          c(1,-1,1,-1,-1,1,-1,1),
          c(1,-1,-1,1,1,-1,-1,1),
          c(1,-1,-1,1,-1,1,1,-1))

for (i in 1:8){
  v[[i]] <- v[[i]]/sqrt(sum(v[[i]]*v[[i]]))
}

alpha <- c(0.9,0.8,0.7,0.6,0.5,0.4,0.3,0.2)

n <- 8

W <- matrix(rep(0,n*n), nrow=n)

for (i in 1:8){
  W <- W + alpha[i]*v[[i]] %*% t(v[[i]])
}

print(eigen(W), digits=2)

